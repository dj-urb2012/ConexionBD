    private void mostrarEnTF(int fila) {
        if(!tablaAutor.getLista().isEmpty()) {
            TfNombre.setText(tablaAutor.getLista().get(fila).getNombrePila());
            TfApellido.setText(tablaAutor.getLista().get(fila).getApellidoPaterno());
            idAutor = tablaAutor.getLista().get(fila).getIdAutor();
            
            int ultRegistro = tablaAutor.getLista().size();
            Icontador.setText("" + (fila + 1) + " de " + ultRegistro);
            TfNombre.requestFocus();
        } else {
            limpiar();
        }
        
    }
    
