    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        if(TfNombre.getText().equals(null)) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre", "Autor", JOptionPane.WARNING_MESSAGE);
            this.TfNombre.requestFocus();
            return;
        }
        if(TfApellido.getText().equals(null)) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un apellido", "Autor", JOptionPane.WARNING_MESSAGE);
            this.TfApellido.requestFocus();
            return;
        }
        int b;
        if(esNuevo) {
            b = tablaAutor.agregarALista(TfNombre.getText(), TfApellido.getText());
        } else {
            b = tablaAutor.editarEnLista(idAutor, TfNombre.getText(), TfApellido.getText());
        }            
        if(b == 1) {
            JOptionPane.showMessageDialog(this, "Registro guardado...", "Autor", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_BtnGuardarActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        int resp = JOptionPane.showConfirmDialog(this, "Â¿Realmente desea eliminar el autor?" + this.TfNombre.getText() + 
                " " + this.TfApellido.getText() + "?", "Autor", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(resp == 0) {
            int b = tablaAutor.eliminarEnLista(idAutor);
            if(b == 1) {
                JOptionPane.showMessageDialog(this, "Registro eliminado satisfactoriamente", "Autor", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Error inespedado, verifique y vuelva a intentarlo", "Autor", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

    private void BtnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAnteriorActionPerformed
        if(posActual == 0) posActual = tablaAutor.getLista().size();
        posActual--;
        this.mostrarEnTF(posActual);
    }//GEN-LAST:event_BtnAnteriorActionPerformed

    private void BtnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSiguienteActionPerformed
        posActual++;
        if(posActual == tablaAutor.getLista().size()) posActual = 0;
        this.mostrarEnTF(posActual);
    }//GEN-LAST:event_BtnSiguienteActionPerformed

    private void BtnGuardarEnBDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarEnBDActionPerformed
        String msn = tablaAutor.actualizarBD();
        JOptionPane.showMessageDialog(this, msn, "Actualizar Base de Datos", JOptionPane.INFORMATION_MESSAGE);
        posActual = 0;
        mostrarEnTF(posActual);
    }//GEN-LAST:event_BtnGuardarEnBDActionPerformed

